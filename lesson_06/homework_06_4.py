# Є ліст з числами, порахуйте сумму усіх ПАРНИХ чисел в цьому лісті

# Створила довільний список із числами

random_numbers = [12, 5, 78, 34, 9, 61, 27, 43, 88, 1]

# Створила змінну для підрахунку суми парних чисел

even_numbers_sum = 0

# Перебираю числа у списку, відбираю парні і сумую їх

for number in random_numbers:
    if number % 2 == 0:
        even_numbers_sum += number

print(f"Сума усіх парних чисел = {even_numbers_sum}")
